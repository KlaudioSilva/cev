# Exercício 007 - Crie um programa que leia as duas notas de um
# aluno, calcule e mostre a sua média.
# Klaudio Silva.

n1 = float(input('Digite o valor da primeira nota: '))
n2 = float(input('Digite o valor da segunda nota: '))
med = (n1 + n2) / 2

print('Com as notas {} e {}, o aluno teve média {}.'.format(n1, n2, med))
print()
